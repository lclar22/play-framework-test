PAQUETE DE INVENTARIOS
	El paquete de Inventarios Khipu te ofrece una relación detallada de las existencias materiales incluidas en el activo, mostrando el número de unidades en existencia, la descripción de los artículos, los precios unitarios, el importe de cada categoría o grupo, así como el total del inventario, con posibilidad de registro mediante lector de código de barras.
	Khipu se rige bajo los métodos de valoración PEPS, UEPS o Promedios, ofreciéndote reportes totalmente parametrizables según los requerimientos de cada momento.
	Se trata de un software de Inventarios online seguro, sencillo e inteligente, que le obsequia con predicciones, avisos y vencimientos, contribuyendo a su organización económica de una forma sencilla, clara y precisa. Sin limitación de tiempo y con acceso 24 horas al día, los 365 días del año a través de Pc o laptop, tablet y celular.

